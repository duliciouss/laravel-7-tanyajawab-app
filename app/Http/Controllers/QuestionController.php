     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $attr = $request->validate([
            'question_title' => 'required|max:255',
            'question_body' => 'required|max:255',
            'question_tag' => 'required|max:255'
        ]);

        Auth::user()->question()->create($attr, [
            'user_id' => Auth::user()->id
        ]);

        return redirect()->route('question.index')->with('status', 'Your question is succesfully created!');
    }

